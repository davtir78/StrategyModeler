const ICON_NAMES = Object.keys(ICONS);

// Return the full <svg> markup string for a name (for innerHTML / PDF embedding). "" if unknown.
function iconMarkup(name, size = 20) {
  const inner = ICONS[name];
  if (!inner) return "";
  return `<svg viewBox="0 0 24 24" width="${size}" height="${size}" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${inner}</svg>`;
}

// Return an inline element rendering the icon. Falls back to raw text (e.g. an emoji) for
// names not in the set, so custom glyphs still work. Returns null when there is nothing to show.
function renderIcon(name, { size = 20, color } = {}) {
  if (!name) return null;
  const span = document.createElement("span");
  span.className = "icon";
  if (color) span.style.color = color;
  const markup = iconMarkup(name, size);
  if (markup) span.innerHTML = markup;
  else span.textContent = name; // emoji / custom fallback
  return span;
}

// Modal icon picker. onPick(nameOrEmpty) is called with "" when "No icon" is chosen.
function openIconPicker(current, onPick) {
  const api = openModal({
    title: "Choose an icon",
    wide: true,
    render: (body) => {
      const search = h("input", { type: "text", placeholder: "Search icons…", style: { width: "100%", marginBottom: "12px", padding: "8px 10px", border: "1px solid var(--border)", borderRadius: "6px" } });
      const grid = h("div.icon-grid");
      body.appendChild(search);
      body.appendChild(grid);

      const cell = (name) => {
        const btn = h("button.icon-cell" + (name === current ? ".selected" : ""), {
          type: "button", title: name || "No icon",
          onclick: () => { onPick(name); api.close(); },
        });
        if (name) { btn.innerHTML = iconMarkup(name, 22); }
        else { btn.textContent = "∅"; btn.classList.add("icon-none"); }
        btn.appendChild(h("span.icon-cell-label", { text: name || "None" }));
        return btn;
      };

      function draw(q) {
        grid.innerHTML = "";
        grid.appendChild(cell("")); // "No icon"
        ICON_NAMES.filter((n) => !q || n.includes(q)).forEach((n) => grid.appendChild(cell(n)));
      }
      draw("");
      search.addEventListener("input", () => draw(search.value.trim().toLowerCase()));
      setTimeout(() => search.focus(), 30);
    },
    footer: [h("button.btn", { text: "Cancel", onclick: () => api.close() })],
  });
}


SM.icons = { iconMarkup, renderIcon, openIconPicker, ICONS, ICON_NAMES };
